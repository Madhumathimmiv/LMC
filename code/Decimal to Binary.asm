//Maximum 2^9
INP
	STA NUM
LOOP LDA NUM
	BRZ DISP
	SUB ONE
	BRZ DISP
	BRA DIVIDE
DIVIDE  LDA TWO
        SUB NUM
        SUB ONE
        BRP QOUT
        LDA NUM
        SUB TWO
        STA NUM
        LDA QUOTIENT
        ADD ONE
        STA QUOTIENT
        BRA DIVIDE
QOUT    LDA NUM
        STA @PTR
	LDA PTR
	SUB ONE
	STA PTR
        LDA QUOTIENT
        STA NUM
	LDA ZERO
	STA QUOTIENT
        BRA LOOP
DISP	LDA NUM
	STA @PTR
DCONT LDA NN
	SUB PTR
	ADD ONE
	BRZ EXIT
	LDA @PTR
	OUT
	LDA PTR
	ADD ONE
	STA PTR
	BRA DCONT
EXIT	HLT
NUM DAT
NN DAT 99
PTR DAT 99
ZERO DAT 0
TWO DAT 2
ONE DAT 1
QUOTIENT DAT 0
